class Sprites():

    def __init__(self):
        self.atacar = []
        self.caminar = []
        self.morir = []
        self.castillo = []

    def setSpritesAtacar(self, atacar):
        self.atacar = atacar

    def setSpritesCaminar(self, caminar):
        self.caminar = caminar

    def setSpritesMorir(self, morir):
        self.morir = morir

    def setSpritesCastillo(self, castillo):
        self.castillo = castillo

